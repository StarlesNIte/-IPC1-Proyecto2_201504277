    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        if (buscartext.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un ID para buscar primero");
        } else {
            try {
                cliente = new Cliente();
                cliente.setCodigo(Integer.parseInt(buscartext.getText()));
                cliente.setNombre(nombretext.getText());
                cliente.setApellido(apellidotext.getText());
                cliente.setCodigoTarjeta(Integer.parseInt(tarjetatext.getText()));
                cliente.setFechaNacimiento(nacimientotext.getText());
                cliente.setTelefono(Integer.parseInt(telefonotext.getText()));
                cliente.setMovil(Integer.parseInt(moviltext.getText()));
                cliente.setClasificacionFrecuencia("Anual");
                clientes.add(cliente);
                log.info("cliente " + cliente.getCodigo() +  " Creado");
            } catch (NumberFormatException e) {
                log.error(e);
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

