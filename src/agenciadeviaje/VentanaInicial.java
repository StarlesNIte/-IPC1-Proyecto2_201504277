    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        int i,j;
        String[] datos = new String[clientes.size()];
        ArrayList<Cliente> auxiliar = new ArrayList<>();
        auxiliar = clientes;
        log.info("Preparando datos a reportar");
        for (i = 0; i < auxiliar.size() - 1; i++) {
            for (j = 0; j < auxiliar.size() - i - 1; j++) {
                if (auxiliar.get(j + 1).getCodigo() < auxiliar.get(j).getCodigo()) {
                    Cliente aux2 = auxiliar.get(j + 1);
                    auxiliar.set(j + 1, auxiliar.get(j));
                    auxiliar.set(j, aux2);
                }
            }
        }
        for(i=0;i<clientes.size();i++){
            datos[i] = String.valueOf(auxiliar.get(i).getCodigo()) + ", "+auxiliar.get(i).getNombre() + ", " + auxiliar.get(i).getApellido();
            log.info("preparando dato " + i);
        }
        crearPagina("Cliente por ID.html",datos);
        abrir("Cliente por ID.html");
    }//GEN-LAST:event_jMenuItem2ActionPerformed

