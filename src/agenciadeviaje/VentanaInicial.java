    private void buscarbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbuttonActionPerformed
        // TODO add your handling code here:
        if (clientes.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay clientes");
        } else {
            for (int i = 0; i < clientes.size(); i++) {
                if (clientes.get(i).getCodigo() == Integer.parseInt(buscartext.getText())) {
                    nombretext.setText(clientes.get(i).getNombre());
                    apellidotext.setText(clientes.get(i).getApellido());
                    tarjetatext.setText(String.valueOf(clientes.get(i).getCodigoTarjeta()));
                    nacimientotext.setText(clientes.get(i).getFechaNacimiento());
                    telefonotext.setText(String.valueOf(clientes.get(i).getTelefono()));
                    moviltext.setText(String.valueOf(clientes.get(i).getMovil()));
                    i = clientes.size();
                } else if (i == clientes.size() - 1 && Integer.parseInt(buscartext.getText()) != clientes.get(i).getCodigo()) {
                    JOptionPane.showMessageDialog(null, "Tal usuario no existe, registralo como nuevo por favor.");
                }
            }
        }
    }//GEN-LAST:event_buscarbuttonActionPerformed

    private Cliente buscarCliente(int index) {
        Cliente aux = new Cliente();
        for (int i = 0; i < clientes.size(); i++) {
            if (clientes.get(i).getCodigo() == index) {
                aux = clientes.get(i);
            }
        }
        return aux;
    }
