    private void crearPagina(String localFile, String[] lista) {
        FileWriter filewriter = null;
        PrintWriter printw = null;
        int i;
        try {
            filewriter = new FileWriter(localFile);
            printw = new PrintWriter(filewriter);
            printw.println("<html>");
            printw.println("<head><title>" + localFile + "</title></head>");
            printw.println("<body bgcolor=\"#99CC99\">");
            for (i = 0; i < lista.length; i++) {
                printw.println("<center><h1><font color=\"navy\">" + lista[i] + "</font></h1></center>");
                log.info(localFile + "Imprimiento dato " + i);
            }
            printw.println("</body>");
            printw.println("</html>");
            printw.close();
            log.info("Reporte " +localFile + " finalizado");
        } catch (FileNotFoundException e) {
            log.error(e);
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(VentanaInicial.class.getName()).log(Level.SEVERE, null, ex);
            log.error(ex);
        }
    }
            
    
